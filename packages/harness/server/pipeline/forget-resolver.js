// FORGET resolution helper — VESTA-SPEC-134 §3.4 — Phase 3
//
// Resolves a <<FORGET: target>> signal against the UserMemories collection:
//   - If target looks like a memory _id → exact-match retire
//   - If target looks like a topic keyword → find all active memories for
//     (entity, user_id) whose `topic` contains the keyword (case-insensitive),
//     retire the 20 oldest; log excess count at info level
//
// Maximum 20 documents retired per call (SPEC-134 §3.4).
// Excess count logged at info level — not surfaced to user.
//
// API:
//   KoadHarnessForgetResolver.resolve(target, { entity, userId }) → Promise<{ retired, excess }>
//     retired: Number of documents retired
//     excess:  Number of matching documents beyond the 20 limit (logged + returned)

'use strict';

const MAX_FORGET = 20;

// Heuristic: a memory _id looks like "mem_<alphanum>_<alphanum>" or similar
// (generated by Random.id() or our generateMemoryId helper).
// Anything that doesn't look like a _id is treated as a topic keyword.
// We don't need perfect discrimination — exact-match _id lookup is fast;
// on miss we fall through to topic search.
function looksLikeMemoryId(target) {
  // _id from Mongo Random.id(): 17 chars, alphanumeric
  // Our generateMemoryId: "mem_<base36>_<base36>"
  // Match either pattern
  return /^[a-zA-Z0-9_]{10,}$/.test(target);
}

KoadHarnessForgetResolver = {

  // resolve(target, { entity, userId }) → Promise<{ retired, excess }>
  async resolve(target, { entity, userId }) {
    if (!globalThis.UserMemoriesCollection) {
      console.warn('[harness:forget-resolver] UserMemoriesCollection not available');
      return { retired: 0, excess: 0 };
    }

    const col = globalThis.UserMemoriesCollection;
    const now = new Date();

    // ── Exact _id match path ────────────────────────────────────────────────────
    if (looksLikeMemoryId(target)) {
      const doc = await col.findOneAsync({
        _id:          target,
        user_id:      userId,
        entity,
        superseded_at: null,
        forgotten_at:  null,
      });

      if (doc) {
        await col.updateAsync({ _id: target }, { $set: { forgotten_at: now } });
        return { retired: 1, excess: 0 };
      }
      // Fall through to topic search if exact _id not found
    }

    // ── Topic keyword search path ───────────────────────────────────────────────
    const activeDocs = await col.find({
      user_id:       userId,
      entity,
      superseded_at: null,
      forgotten_at:  null,
    }).fetchAsync();

    // Filter by topic keyword (case-insensitive substring match)
    const keyword = target.toLowerCase();
    const matching = activeDocs.filter(doc =>
      doc.topic && doc.topic.toLowerCase().includes(keyword)
    );

    if (matching.length === 0) {
      return { retired: 0, excess: 0 };
    }

    // Sort oldest first by captured_at (retire oldest 20)
    matching.sort((a, b) => new Date(a.captured_at) - new Date(b.captured_at));

    const toRetire = matching.slice(0, MAX_FORGET);
    const excess   = Math.max(0, matching.length - MAX_FORGET);

    if (excess > 0) {
      console.info(
        `[harness:forget-resolver] FORGET "${target}": ${matching.length} matching memories, ` +
        `retiring ${MAX_FORGET} oldest, ${excess} left active (limit=${MAX_FORGET})`
      );
    }

    // Retire the selected docs
    const ids = toRetire.map(d => d._id);
    for (const id of ids) {
      await col.updateAsync({ _id: id }, { $set: { forgotten_at: now } });
    }

    return { retired: toRetire.length, excess };
  },
};
